    public void setUpPanelTypeEmple(){
        JFrame.putFilterEmpleadoPanel(getTypeEmple());
    }
    
    // uso boolean por que solo hay dos tipos, pero obv si hubiera mas pasario un valor cada uno significando un tipo de Empleado
    public boolean getTypeEmple(){
        int tipoEmple = jcombTypeEmple.getSelectedIndex();
        
        if(tipoEmple>=0){
            if(typeEmpleados.get(tipoEmple).equals("Analista")){
                return false;
            }
        }
        // por defecto sera programador
        return true;
    }

    public void setUpFilter(){
        jcombTypeEmple.removeAllItems();
        for(String part: typeEmpleados){
            jcombTypeEmple.addItem(part);
        }
    }
    
